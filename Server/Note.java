import java.util.* ;

public class Note {
  public final String color;
  public final String message;
  public final int x;
  public final int y;
  public final int width;
  public final int height;
  public boolean pinned;

  public Note(int x, int y, String color, String message) {
    this.color = color;
    this.message = message;
    this.x = x;
    this.y = y;
    this.width;
    this.height;
    this.pinned = pinned;
  }
}
